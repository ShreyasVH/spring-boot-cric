CREATE TABLE totals
(
    id NUMBER(10) GENERATED BY DEFAULT AS IDENTITY,
    match_id NUMBER(10) NOT NULL,
    team_id NUMBER(19) NOT NULL,
    runs NUMBER(5) DEFAULT 0 NOT NULL,
    wickets NUMBER(5) DEFAULT 0,
    balls NUMBER(5) DEFAULT 0 NOT NULL,
    innings NUMBER(5) NOT NULL,
    CONSTRAINT pk_totals PRIMARY KEY (id),
    CONSTRAINT uk_t_match_team_innings UNIQUE (match_id, team_id, innings),
    CONSTRAINT fk_t_match FOREIGN KEY (match_id) REFERENCES matches (id),
    CONSTRAINT fk_t_team FOREIGN KEY (team_id) REFERENCES teams (id)
);

CREATE INDEX idx_totals_team_id ON totals (team_id);

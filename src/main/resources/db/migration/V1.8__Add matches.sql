CREATE TABLE matches
(
    id NUMBER(10) GENERATED BY DEFAULT AS IDENTITY,
    series_id NUMBER(19) NOT NULL,
    team_1_id NUMBER(19) NOT NULL,
    team_2_id NUMBER(19) NOT NULL,
    toss_winner_id NUMBER(19),
    bat_first_id NUMBER(19),
    result_type_id NUMBER(5) NOT NULL,
    winner_id NUMBER(19),
    win_margin NUMBER(5),
    win_margin_type_id NUMBER(5),
    stadium_id NUMBER(19) NOT NULL,
    start_time TIMESTAMP NOT NULL,
    is_official NUMBER(1) DEFAULT 1 NOT NULL,
    CONSTRAINT pk_matches PRIMARY KEY (id),
    CONSTRAINT ck_matches_is_official CHECK (is_official IN (0, 1)),
    CONSTRAINT uk_m_stadium_start UNIQUE (stadium_id, start_time),
    CONSTRAINT fk_m_series FOREIGN KEY (series_id) REFERENCES series (id),
    CONSTRAINT fk_m_team_1 FOREIGN KEY (team_1_id) REFERENCES teams (id),
    CONSTRAINT fk_m_team_2 FOREIGN KEY (team_2_id) REFERENCES teams (id),
    CONSTRAINT fk_m_toss_winner FOREIGN KEY (toss_winner_id) REFERENCES teams (id),
    CONSTRAINT fk_m_bat_first FOREIGN KEY (bat_first_id) REFERENCES teams (id),
    CONSTRAINT fk_m_result_type FOREIGN KEY (result_type_id) REFERENCES result_types (id),
    CONSTRAINT fk_m_winner FOREIGN KEY (winner_id) REFERENCES teams (id),
    CONSTRAINT fk_m_win_margin_type FOREIGN KEY (win_margin_type_id) REFERENCES win_margin_types (id),
    CONSTRAINT fk_m_stadium FOREIGN KEY (stadium_id) REFERENCES stadiums (id)
);



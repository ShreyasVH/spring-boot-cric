CREATE TABLE tags_map
(
    id NUMBER(19) GENERATED BY DEFAULT AS IDENTITY,
    entity_type VARCHAR2(100) NOT NULL,
    entity_id NUMBER(10) NOT NULL,
    tag_id NUMBER(5) NOT NULL,
    CONSTRAINT pk_tags_map PRIMARY KEY (id),
    CONSTRAINT uk_tm_type_id_tag UNIQUE (entity_type, entity_id, tag_id),
    CONSTRAINT fk_tm_tag FOREIGN KEY (tag_id) REFERENCES tags (id)
);
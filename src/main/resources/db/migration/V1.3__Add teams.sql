CREATE TABLE team_types
(
    id NUMBER(5) GENERATED BY DEFAULT AS IDENTITY,
    name VARCHAR2(100) NOT NULL,
    CONSTRAINT pk_team_types PRIMARY KEY (id),
    CONSTRAINT uk_tt_name UNIQUE (name)
);

